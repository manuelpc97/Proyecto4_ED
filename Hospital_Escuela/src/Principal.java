    public boolean verifificarNombre(ArrayList<Ubicacion> lista, String nombre) {
        boolean seguir = true;
        for (int i = 0; i < lista.size(); i++) {
            if (lista.get(i).getNombre() == nombre) {
                seguir = false;
            }
        }
        return seguir;
    }

